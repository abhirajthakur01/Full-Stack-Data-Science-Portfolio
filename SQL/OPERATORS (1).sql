-- DATE_FORMAT():
/* USED TO FORMAT A DATE/DATETIME VALUE INTO A SPECIFIC FORMAT.
IT ALLOWS US TO CONVERT A DATE/DATETIME VALUE TO A FORMATTED STRING.

ALL FORMATTING TYPE:
1. "%Y" = 4 digit year
2. "%y" = 2 digit year
3. "%M" = month name
4. "%m" = numeric month (2 digit )
5. "%c" =  numeric month (1 or 2 digit)
6. "%d" = day of month ( 2 digit)
7. "%b" = abbreviated month name (eg. Jan)
8. "%W" = weekday name (eg. sunday)
9. "%a" = abbreviated day name(eg. sun)
10. "%H" = Hour (00-23 format)
11. "%h" or "%I" = Hour (01-12)
12. "%l" = Hour (1-12)
13. "%i"  = minutes(00-59)
14. "%s" = seconds
15. "%p" = AM/pm
*/


