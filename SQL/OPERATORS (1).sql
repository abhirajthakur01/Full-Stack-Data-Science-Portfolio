-- ORDER BY: USED TO SORT THE RESULT-SET IN ASCENDING OR
-- IN DESCENDING ORDER.
-- BY DEFAULT IT CONSIDER IN ASCENDING ORDER.
-- FOR ASCENDING WE USE "ASC" AND FOR DESCENDING "DESC".

